//import java.util.*;
public class typecasting{
    public static void main(String [] args){
        int x=89;
        double y=x;
        System.out.println(y); //Automatic Typecasting


        double ankit=89.15;
        int minal=(int) ankit;
        System.out.println(minal); //Manual Typecasting

        // byte->short->char->int->long->float->double
    }
}